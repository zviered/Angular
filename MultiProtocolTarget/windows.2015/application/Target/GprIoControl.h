#define CTL_CODE( DeviceType, Function, Method, Access ) (                 \
    ((DeviceType) << 16) | ((Access) << 14) | ((Function) << 2) | (Method) \
)

#define METHOD_BUFFERED                 0
#define METHOD_IN_DIRECT                1
#define METHOD_OUT_DIRECT               2
#define METHOD_NEITHER                  3

#define FILE_ANY_ACCESS                 0
#define FILE_SPECIAL_ACCESS    			(FILE_ANY_ACCESS)
#define FILE_READ_ACCESS          		( 0x0001 )    // file & pipe
#define FILE_WRITE_ACCESS         		( 0x0002 )    // file & pipe

#define FILE_DEVICE_UNKNOWN         	0x00000022

#define MODULE_READ_LONG_CODE			CTL_CODE		(FILE_DEVICE_UNKNOWN, 0x802, METHOD_BUFFERED,	FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define MODULE_WRITE_LONG_CODE			CTL_CODE		(FILE_DEVICE_UNKNOWN, 0x803, METHOD_BUFFERED,	FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define MODULE_WRITE_CONFIG_CODE		CTL_CODE		(FILE_DEVICE_UNKNOWN, 0x804, METHOD_BUFFERED,	FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define MODULE_READ_CONFIG_CODE			CTL_CODE		(FILE_DEVICE_UNKNOWN, 0x805, METHOD_BUFFERED,	FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define MODULE_DATA_SOURCE_CODE			CTL_CODE		(FILE_DEVICE_UNKNOWN, 0x806, METHOD_BUFFERED,	FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define MODULE_READ_DATA_CODE			CTL_CODE		(FILE_DEVICE_UNKNOWN, 0x807, METHOD_OUT_DIRECT ,	FILE_ANY_ACCESS)

